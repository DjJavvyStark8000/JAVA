    private void btnSaveEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveEmpActionPerformed
        BL_Empleado logicaEmpleado = new BL_Empleado();
        Ent_Empleado empleado = crearEmpleado();
        if (!areFieldsEmpty()) {//verificar que todos los campos esten llenos
            try {
                if (empleado.isExiste()) {//hay empleados para modificar
                    logicaEmpleado.callModificarEmpleado(empleado);
                } else {
                    logicaEmpleado.callInsertarEmpleado(empleado);
                }
                JOptionPane.showMessageDialog(this, logicaEmpleado.getMessage());
                cleanForm();
                cargarTabla("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Faltan datos...");
        }
    }//GEN-LAST:event_btnSaveEmpActionPerformed

    private void tblEmployeesDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmployeesDataMouseClicked
        BL_Empleado logica = new BL_Empleado();//para llamar a obtener un empleado
        Ent_Empleado empleado;//guardar el empleado
        String condition; //lo que vamos a buscar para obtener de la base de datos
        try {
            if (evt.getClickCount() == 2) {
                int fila = tblEmployeesData.rowAtPoint(evt.getPoint());//detectar la fila
                txtEmployeeID.setText(tblEmployeesData.getValueAt(fila, 0).toString());//detectar la primera columna
                condition = String.format("ID_EMPLEADO = %s", txtEmployeeID.getText());//lo que vamos a buscar
                empleado = logica.callObtenerEmpleado(condition);
                //mover datos de la tabla a las cajas de textp             
                txtEmployeeName.setText(empleado.getNombre());
                txtEmployeeAddress.setText(empleado.getDireccion());
                txtEmployeePhone.setText(empleado.getTelefono());
                txtEmployeeEmail.setText(empleado.getCorreo());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_tblEmployeesDataMouseClicked

