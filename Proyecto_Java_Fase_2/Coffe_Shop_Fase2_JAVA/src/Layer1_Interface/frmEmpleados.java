    private void cleanForm() {
        txtEmployeeID.setText("");
        txtEmployeeName.setText("");
        txtEmployeeAddress.setText("");
        txtEmployeePhone.setText("");
        txtEmployeeEmail.setText("");
        cboEmployeePosition.setSelectedIndex(0);
        cleanTable();
    }

    private void printInfo() {
        if (!txtEmployeeID.getText().isEmpty()) {
            String employeeID = txtEmployeeID.getText();
            String employeeName = txtEmployeeName.getText();
            String employeeAddress = txtEmployeeAddress.getText();
            String employeePhone = txtEmployeePhone.getText();
            String employeeEmail = txtEmployeeEmail.getText();
            String employeePosition = cboEmployeePosition.getSelectedItem().toString();

            String message = "Employee ID: " + employeeID + "\n"
                    + "Employee Name: " + employeeName + "\n"
                    + "Employee Address: " + employeeAddress + "\n"
                    + "Employee Phone: " + employeePhone + "\n"
                    + "Employee Email: " + employeeEmail + "\n"
                    + "Employee Position: " + employeePosition;

            JOptionPane.showMessageDialog(this, message, "Employee Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private boolean areFieldsEmpty() {
        // Verificar si alguna de las casillas está vacía
        return txtEmployeeName.getText().isEmpty()
                || txtEmployeeAddress.getText().isEmpty()
                || txtEmployeePhone.getText().isEmpty()
                || txtEmployeeEmail.getText().isEmpty();
    }

    private void cleanTable() {//table titles override
        theTableModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        //fix table with named columns
        tblEmployeesData.setModel(theTableModel);
        //add columns
        theTableModel.addColumn("ID");
        theTableModel.addColumn("NOMBRE");
        theTableModel.addColumn("DIRECCIÓN");
        theTableModel.addColumn("TELÉFONO");
        theTableModel.addColumn("CORREO");
        theTableModel.addColumn("ID_CARGO");
    }

    private void cargarTabla(String condition) {//load data into table from database
        try {
            BL_Empleado empleadologic = new BL_Empleado();
            List<Ent_Empleado> listempleado;
            cleanTable();
            Object[] therow = new Object[6];
            listempleado = empleadologic.callListarEmpleados(condition);

            for (Ent_Empleado empleado : listempleado) {
                therow[0] = empleado.getIdEmpleado();
                therow[1] = empleado.getNombre();
                therow[2] = empleado.getDireccion();
                therow[3] = empleado.getTelefono();
                therow[4] = empleado.getCorreo();
                therow[5] = empleado.getId_cargo();

                theTableModel.addRow(therow);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ha sucedido un error: " + e.getMessage());
        }
    }

    private Ent_Empleado crearEmpleado() {
        Ent_Empleado empleado = new Ent_Empleado();

        if (!txtEmployeeID.getText().isEmpty()) {
            empleado.setExiste(true);//Empleado existe          
            empleado.setIdEmpleado(Integer.parseInt(txtEmployeeID.getText()));//El id del objeto
        }
        empleado.setNombre(txtEmployeeName.getText());
        empleado.setDireccion(txtEmployeeAddress.getText());
        empleado.setTelefono(txtEmployeePhone.getText());
        empleado.setCorreo(txtEmployeeEmail.getText());
        //por ahora no exite una interfaz para manejar los cargos
        //temporalmente en la base de datos existen los  cargos y sus ids
        //pero no son autogenerados sino que cada id es representado por
        // el index + 1 del cboEmployeePosition
        int theIndex = cboEmployeePosition.getSelectedIndex();
        int idCargo = theIndex + 1;
        empleado.setId_cargo(idCargo);

        return empleado;
    }
    
